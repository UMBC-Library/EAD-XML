                     <h1 id="adminbiog">Administrative/Biographical Note</h1>
                     <p>R.D. "Bob" Allen (1927-1986) was a professor of biology at Dartmouth College, the
                        founder of the journal "Cell Motility and the Cytoskeleton" (renamed "Cytoskeleton"
                        in 2010), and leader in the field of cell motility, cytomechanics, and the developing
                        use of video microscopy and optics in scientific research. Allen died in his home
                        in New Hampshire, March 23, 1986, after a battle with pancreatic cancer.
                     </p>
