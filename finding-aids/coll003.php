                     <h1 id="adminbiog">Administrative/Biographical Note</h1>
                     <p>Jane Yolen (1939 - ) is a science fiction and children's book author. Yolen has received
                        both the Nebula Award from the Science Fiction/ Fantasy Writers of America and the
                        Mythopoeic Fantasy Award. Harlan Ellison (1934 - ) is a science fiction author who
                        has also worked in television, radio, and comics and graphic narratives. Ellison is
                        also a recepient of the Nebula Award, in addition to the World Science Fiction Society's
                        Hugo Award and the Silver Pen for Journalism by PEN International.
                     </p>
