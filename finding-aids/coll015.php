                        comprising one folder, was delivered by Dr. Joan Bennett on Dec 13, 2002.
                     </p>
                     <h2>Processing Note</h2>
                     <p>The records transferred from Northwestern had been partially arranged and described
                        and had come with a finding aid to the processed portion; for the remaining portion
                        there were accession records but no inventory. The second accession however had no
                        inventory and was found to be in a disordered state. An inventory was done of all
                        unprocessed materials and all records whether previously processed or not were organized
                        according to the administrative arrangement set out in the SIM Handbook and taking
                        into account the initial order done by Northwestern. All documents pertaining to the
                        provenance, accessions, and processing of materials at Northwestern University may
                        be found under the series Affiliated Organizations Envelopes, invoices, cancelled
                        checks, payroll information such as time sheets and duplicate copies have been weeded.
                        Processing was completed and finding aid written by Marcia Frank Peri, December 2002.
                     </p>
