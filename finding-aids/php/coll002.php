                              <td>This collection contains the papers of the Perry Hall Improvement Association (PHIA),
                                 a local community advocacy group founded in 1945 by the residents of Perry Hall, Maryland,
                                 a residential community located in Baltimore County. The collection contains by-laws,
                                 histories, rosters, correspondence, minutes, research and case files, financial and
