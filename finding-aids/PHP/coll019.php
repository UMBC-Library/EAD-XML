                     <h1 id="adminbiog">Administrative/Biographical Note</h1>
                     <p>The Maryland Biotechnology Institute was authorized at the University of Maryland
                        by the General Assembly in 1985. It brought together different research disciplines
                        across the biotechnology field within the University System of Maryland and provided
                        expertise along with exceptional facilities for training, research, and development.
                        The Institute comprised four unique research centers that collaborated and cooperated
                        with various other government and academic institutions. The Institute was closed
                        and restructured in 2010.
                     </p>
