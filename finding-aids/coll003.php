                     <h1 id="provinfo">Provenance Information</h1>
                     <h2>Provenance and Acquisition Information</h2>
                     <p>These papers were acquired as three separate donations along with several books, some
                        photographs and a large collection of comic books from Rita Seplowitz Saltz. Accessions
                        from Saltz include: MSS 1995-09, 1996-13, 1996-14, 1997-05, 1998-01.
                     </p>
                     <h2>Processing Note</h2>
                     <p>Collection unprocessed. Several donations by Saltz contained science fiction publications;
                        these have been removed and added to the Rosenfeld Science Fiction Collection or the
                        general stacks.
