                     <h1 id="adminbiog">Administrative/Biographical Note</h1>
                     <p>Born circa 1823, Hugh Bolton Jones was the father of the well-known Maryland landscape
                        painter, Hugh Bolton Jones (1848-1927), Francis and Louise. He was married to Laura
                        Jones. The senior Jones resided in Baltimore, at least during the early 1880s.
                     </p>
                     <hr></hr>
                     <h1 id="scope">Scope &amp; Content</h1>
                     <h2>Arrangement</h2>
                     <p>Arranged chronologically. Letters that do not clearly list a year are filed at the
                        end of the collection.
                     </p>
                     <h2>Note</h2>
                     <p>The Hugh Bolton Jones letters range in date from 1880 to 1881, with three undated
                        items. This collection of correspondence includes 20 letters with envelopes and 6
                        envelopes without accompanying letters, comprising .5 linear feet. 
                     </p>
                     <p>The collection contains the correspondence of Hugh Bolton Jones or his wife, Laura,
                        from their children, relatives, friends, and acquaintances. Envelopes are addressed
                        to Hugh Bolton Jones. Those who corresponded with Jones include Hugh Bolton Jones,
                        their son; Louise, their daughter; Lydia A. Hamilton, a relative; [Hank] Jones, Hugh
                        Bolton Jones' brother; Joseph F. Foreman; Francis A. Crook; and E.M. Stephenson. 
                     </p>
                     <p>The content of the letters includes personal correspondence and business relationships.
                        Several of the letters reference an exchange of money. The bulk of the letters, however,
                        are the private mail between child and parent. Four letters from Hugh Bolton Jones
                        to his father include his signature, "H. Bolton Jones." 
                     </p>
                     <p>One letter in particular seems to have been written to the son, who was a well-known
                        Maryland landscape painter; this letter describes an acquaintance's visit to a French
                        Salon show, including descriptions of paintings by Jones and several other painters.
                     </p>
